private boolean validasiInput(String username, String password, String konfirmasiPassword, String email, String alamat) {
    boolean valid = true;    

    if (username.trim().isEmpty()) {
        lbperingatanusername.setText("Username harus diisi");
        valid = false;
    } else if (!username.matches("[a-zA-Z]+")) {
        lbperingatanusername.setText("Username hanya huruf");
        valid = false;
    } else if (CustomerLogin.isUsernameExist(username)) {
        lbperingatanusername.setText("Username sudah digunakan");
        valid = false;
    }

    if (password.trim().isEmpty()) {
        lbperingatanpassword.setText("Password harus diisi");
        valid = false;
    } else if (password.length() < 8 || password.length() > 16) {
        lbperingatanpassword.setText("Password harus 8-16 karakter");
        valid = false;
    }

    if (konfirmasiPassword.trim().isEmpty()) {
        lbperingatankonfirmasipassword.setText("Konfirmasi harus diisi");
        valid = false;
    } else if (!password.equals(konfirmasiPassword)) {
        lbperingatankonfirmasipassword.setText("Konfirmasi tidak sama");
        valid = false;
    }


    if (email.trim().isEmpty()) {
        lbperingatanemail.setText("Email harus diisi");
        valid = false;
    } else if (!email.endsWith("@gmail.com")) {
        lbperingatanemail.setText("Email harus @gmail.com");
        valid = false;
    } else if (CustomerLogin.isEmailExist(email)) {
        lbperingatanemail.setText("Email sudah digunakan");
        valid = false;
    }

    if (alamat.trim().isEmpty()) {
        lbperingatanalamat.setText("Alamat harus diisi");
        valid = false;
    } else if (alamat.length() > 200) {
        lbperingatanalamat.setText("Alamat maksimal 200 karakter");
        valid = false;
    }

    return valid;
}


