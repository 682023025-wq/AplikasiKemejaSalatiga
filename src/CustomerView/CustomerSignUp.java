    private void btnsignupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsignupActionPerformed
    reset();
    
    String username = txtusername.getText();
    String password = new String(txtpassword.getPassword());
    String konfirmasi = new String(txtkonfrimasipassword.getPassword());
    String email = txtemail.getText();
    String alamat = txtalamat.getText();

    if (validasiInput(username, password, konfirmasi, email, alamat)) {
        String hashedPassword = CustomerLogin.hashPassword(password);
        String hashedKonfirmasi = CustomerLogin.hashPassword(konfirmasi);

        boolean success = CustomerLogin.daftarCustomerBaru(username, hashedPassword, hashedKonfirmasi, email, alamat);
        if (success) {
            JOptionPane.showMessageDialog(this, "Pendaftaran berhasil!");
            new CustomerMenuLogin().setVisible(true);
            dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Pendaftaran gagal.");
        }
    }
    }//GEN-LAST:event_btnsignupActionPerformed

