private void loadKeranjang() {
    try (Connection conn = Koneksi.DBKoneksi.getConnection()) {
        String customerId = CustomerController.CustomerLogin.sessionCustomer.getId();

        String sql = """
            SELECT 
                p.nama AS nama_produk,
                dk.kategori,
                du.ukuran,
                dt.harga,
                dt.jumlah_barang,
                dt.total_harga
            FROM detail_transaksi dt
            JOIN transaksi t ON dt.transaksi_id_transaksi = t.id_transaksi
            JOIN produk p ON dt.produk_id_produk = p.id_produk
            JOIN detail_kategori dk ON p.detail_kategori_id_kategori = dk.id_kategori
            JOIN detail_ukuran du ON p.detail_ukuran_id_ukuran = du.id_ukuran
            JOIN pembayaran pb ON dt.pembayaran_order_id = pb.order_id
            WHERE t.customer_id = ? AND pb.status = 'pending'
            ORDER BY dt.transaksi_id_transaksi
        """;

        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setString(1, customerId);
        ResultSet rs = ps.executeQuery();

        DefaultTableModel model = (DefaultTableModel) tabelkeranjang.getModel();
        model.setRowCount(0); // reset tabel

        int no = 1;
        int totalItem = 0;
        int totalBarang = 0;
        long totalHarga = 0;

        while (rs.next()) {
            int jumlah = rs.getInt("jumlah_barang");
            long total = rs.getLong("total_harga");

            Object[] row = {
                no++,
                rs.getString("nama_produk"),
                rs.getString("kategori"),
                rs.getString("ukuran"),
                rs.getLong("harga"),
                jumlah,
                total
            };
            model.addRow(row);

            totalItem++;
            totalBarang += jumlah;
            totalHarga += total;
        }

        txtjumlahitem.setText("Jumlah Item " + String.valueOf(totalItem));
        txttotalbarang.setText("Total Barang " + String.valueOf(totalBarang));
        txttotal.setText("Rp " + String.format("%,d", totalHarga).replace(',', '.') + ",00");

    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Gagal memuat keranjang: " + e.getMessage());
    }
}



