    private void btnhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapusActionPerformed
       int row = tabelkeranjang.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Pilih dulu produk yang ingin dihapus.");
            return;
        }

        String namaProduk = tabelkeranjang.getValueAt(row, 1).toString();
        int confirm = JOptionPane.showConfirmDialog(this, "Hapus produk '" + namaProduk + "' dari keranjang?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            String customerId = CustomerController.CustomerLogin.sessionCustomer.getId();

            try (Connection conn = DBKoneksi.getConnection()) {
                String sql = "SELECT id_produk FROM produk WHERE nama = ?";
                String produkId = "";
                try (PreparedStatement ps = conn.prepareStatement(sql)) {
                    ps.setString(1, namaProduk);
                    try (ResultSet rs = ps.executeQuery()) {
                        if (rs.next()) {
                            produkId = rs.getString("id_produk");
                        }
                    }
                }

                if (!produkId.isEmpty()) {
                    boolean sukses = CustomerKeranjang.hapusItem(customerId, produkId);
                    if (sukses) {
                        JOptionPane.showMessageDialog(this, "Produk berhasil dihapus.");
                        loadKeranjang();
                    } else {
                        JOptionPane.showMessageDialog(this, "Gagal menghapus produk.");
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "ID produk tidak ditemukan.");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Kesalahan: " + e.getMessage());
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnhapusActionPerformed

    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        int row = tabelkeranjang.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Pilih dulu produk yang mau diupdate.");
            return;
        }

        String namaProduk = tabelkeranjang.getValueAt(row, 1).toString();
        String produkId = "";

        try (Connection conn = DBKoneksi.getConnection()) {
            String sql = "SELECT id_produk, stok FROM produk WHERE nama = ?";
            try (PreparedStatement ps = conn.prepareStatement(sql)) {
                ps.setString(1, namaProduk);
                try (ResultSet rs = ps.executeQuery()) {
                    if (rs.next()) {
                        produkId = rs.getString("id_produk");
                        int stok = rs.getInt("stok");

                        String input = JOptionPane.showInputDialog(this, "Masukkan jumlah baru (maks: " + stok + "):");
                        if (input == null) return;

                        int jumlahBaru;
                        try {
                            jumlahBaru = Integer.parseInt(input);
                        } catch (NumberFormatException e) {
                            JOptionPane.showMessageDialog(this, "Input jumlah tidak valid.");
                            return;
                        }

                        if (jumlahBaru <= 0 || jumlahBaru > stok) {
                            JOptionPane.showMessageDialog(this, "Jumlah tidak boleh 0 atau melebihi stok.");
                            return;
                        }

                        int confirm = JOptionPane.showConfirmDialog(this, "Update jumlah ke " + jumlahBaru + "?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
                        if (confirm == JOptionPane.YES_OPTION) {
                            String customerId = CustomerController.CustomerLogin.sessionCustomer.getId();
                            boolean sukses = CustomerKeranjang.updateJumlahBarang(customerId, produkId, jumlahBaru);
                            if (sukses) {
                                loadKeranjang();
                            } else {
                                JOptionPane.showMessageDialog(this, "Gagal update jumlah.");
                            }
                        }
                    } else {
                        JOptionPane.showMessageDialog(this, "Produk tidak ditemukan.");
                    }
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Kesalahan: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnupdateActionPerformed

    private void btntambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntambahActionPerformed
        int row = tabelkeranjang.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Pilih dulu barangnya.");
            return;
        }

        String namaProduk = tabelkeranjang.getValueAt(row, 1).toString();
        String produkId = "";

        try (Connection conn = DBKoneksi.getConnection()) {
            String sql = "SELECT id_produk FROM produk WHERE nama = ?";
            try (PreparedStatement ps = conn.prepareStatement(sql)) {
                ps.setString(1, namaProduk);
                try (ResultSet rs = ps.executeQuery()) {
                    if (rs.next()) {
                        produkId = rs.getString("id_produk");
                    }
                }
            }

            if (produkId.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Produk tidak ditemukan.");
                return;
            }

            int confirm = JOptionPane.showConfirmDialog(this, "Tambah 1 barang untuk \"" + namaProduk + "\"?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                String customerId = CustomerController.CustomerLogin.sessionCustomer.getId();
                boolean berhasil = CustomerKeranjang.tambahJumlahBarang(customerId, produkId);
                if (berhasil) {
                    loadKeranjang();
                } else {
                    JOptionPane.showMessageDialog(this, "Gagal menambah. Stok sudah habis.", "Stok Limit", JOptionPane.WARNING_MESSAGE);
                }
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Kesalahan: " + ex.getMessage());
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btntambahActionPerformed


