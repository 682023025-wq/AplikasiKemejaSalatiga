    private void btnbayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbayarActionPerformed
    String metodeBaru = (String) pilihanbayar.getSelectedItem();
    
    if (CustomerController.CustomerLogin.sessionCustomer == null) {
        JOptionPane.showMessageDialog(this, "Anda harus login terlebih dahulu.");
        return;
    }

    String customerId = CustomerController.CustomerLogin.sessionCustomer.getId();

    try (Connection conn = Koneksi.DBKoneksi.getConnection()) {
        // 1. Ambil order pending
        String orderId = CustomerController.CustomerPembayaran.getOrderIdPendingByCustomer(conn, customerId);
        if (orderId == null) {
            JOptionPane.showMessageDialog(this, "Tidak ada transaksi pending.");
            return;
        }

        // 2. Cek stok per item langsung berdasarkan tabel produk
        String sqlCekStok = ""
            + "SELECT dp.jumlah_barang, p.stok, p.id_produk, p.nama "
            + "FROM detail_transaksi dp "
            + "JOIN produk p ON dp.produk_id_produk = p.id_produk "
            + "WHERE dp.pembayaran_order_id = ?";

        try (PreparedStatement ps = conn.prepareStatement(sqlCekStok)) {
            ps.setString(1, orderId);
            try (ResultSet rs = ps.executeQuery()) {
                while (rs.next()) {
                    int jumlah     = rs.getInt("jumlah_barang");
                    int stok       = rs.getInt("stok");
                    String idProduk   = rs.getString("id_produk");
                    String namaProduk = rs.getString("nama");

                    if (stok < jumlah) {
                        JOptionPane.showMessageDialog(this,
                            "Transaksi gagal. Stok produk tidak mencukupi.\n\n" +
                            "Detail Produk:\n" +
                            "ID Produk     : " + idProduk + "\n" +
                            "Nama Produk   : " + namaProduk + "\n" +
                            "Stok Saat Ini : " + stok + "\n" +
                            "Jumlah Dibeli : " + jumlah + "\n\n" +
                            "Silakan perbarui keranjang atau hubungi admin.",
                            "Stok Tidak Cukup",
                            JOptionPane.WARNING_MESSAGE);
                        return;
                    }
                }
            }
        }

        // 3. Lanjut ke proses pembayaran
        String metodeLama = CustomerController.CustomerPembayaran.getMetodePembayaran(conn, orderId);
        String metodeMidtransBaru = CustomerController.CustomerPembayaran.mapMetodeToMidtrans(metodeBaru);
        if (metodeMidtransBaru == null) {
            JOptionPane.showMessageDialog(this, "Silakan pilih metode pembayaran yang valid.");
            return;
        }

        if (metodeLama == null) {
            // Belum pilih metode, set lalu proses
            CustomerController.CustomerPembayaran.setMetodePembayaranBaru(conn, orderId, metodeMidtransBaru);
            CustomerController.CustomerPembayaran.prosesMidtrans(conn, orderId, metodeBaru, true);
        } else if (metodeLama.equalsIgnoreCase(metodeMidtransBaru)) {
            // Metode sama, ulang proses
            CustomerController.CustomerPembayaran.prosesMidtrans(conn, orderId, metodeBaru, false);
        } else {
            // Ganti metode
            int confirm = JOptionPane.showConfirmDialog(this,
                "Metode pembayaran sebelumnya adalah '" + metodeLama + "'.\nGanti ke '" + metodeMidtransBaru + "'?",
                "Konfirmasi Ganti Metode", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                String orderIdBaru = CustomerController.CustomerPembayaran.generateOrderIdWithTimestamp();
                boolean updated = CustomerController.CustomerPembayaran.updateMetodeDanOrderId(conn, orderId, orderIdBaru, metodeMidtransBaru);
                if (!updated) {
                    JOptionPane.showMessageDialog(this, "Gagal update metode dan order ID.");
                    return;
                }
                CustomerController.CustomerPembayaran.prosesMidtrans(conn, orderIdBaru, metodeBaru, true);
            } else {
                CustomerController.CustomerPembayaran.prosesMidtrans(conn, orderId, metodeLama, false);
            }
        }

    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage());
    }
    }//GEN-LAST:event_btnbayarActionPerformed

