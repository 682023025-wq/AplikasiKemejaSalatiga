private void loadKeranjang() {
    try (Connection conn = DBKoneksi.getConnection()) {
        // Ambil user ID dari user yang login
        String userId = UserLogin.getSessionUser().getId();

        // Ambil daftar keranjang user
        List<ModelKeranjang> list = UserKeranjang.getKeranjangPending(conn, userId);

        // Set tabel keranjang
        DefaultTableModel model = (DefaultTableModel) tabelkeranjang.getModel();
        model.setRowCount(0);
        for (ModelKeranjang item : list) {
            model.addRow(new Object[]{
                    item.getNo(),
                    item.getNamaProduk(),
                    item.getKategori(),
                    item.getUkuran(),
                    formatRupiah(item.getHarga()),
                    item.getJumlah(),
                    formatRupiah(item.getSubtotal())
            });
        }

        // Set label info
        int jumlahItem = UserKeranjang.getJumlahItem(conn, userId);
        int totalBarang = UserKeranjang.getTotalBarang(conn, userId);
        long totalHarga = UserKeranjang.getTotalHarga(conn, userId);

        txtjumlahitem.setText("Jumlah Item : " + jumlahItem);
        txttotalbarang.setText("Total Barang : " + totalBarang);
        txttotal.setText("Total : " + formatRupiah(totalHarga));

    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Gagal memuat keranjang: " + e.getMessage());
    }
}

    private String formatRupiah(long nominal) {
        java.text.NumberFormat formatter = java.text.NumberFormat.getCurrencyInstance(new java.util.Locale("in", "ID"));
        return formatter.format(nominal);
    }
public String cekTransaksiPending(Connection conn, String userId) throws SQLException {
    String sql = """
        SELECT t.id_transaksi 
        FROM transaksi t
        JOIN detail_transaksi dt ON t.id_transaksi = dt.transaksi_id_transaksi
        JOIN pembayaran p ON dt.pembayaran_order_id = p.order_id
        WHERE t.user_id = ? AND p.status = 'pending'
        ORDER BY p.tanggal DESC
        LIMIT 1
    """;

    try (PreparedStatement ps = conn.prepareStatement(sql)) {
        ps.setString(1, userId);
        try (ResultSet rs = ps.executeQuery()) {
            if (rs.next()) {
                return rs.getString("id_transaksi");
            }
        }
    }
    return null;
}
    
    
