    private void btntambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntambahActionPerformed
    String userId = UserLogin.getSessionUser().getId();
    int row = tabelkeranjang.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Pilih dulu barangnya.");
        return;
    }

    try (Connection conn = DBKoneksi.getConnection()) {
        String produkId = UserKeranjang.getProdukIdByRow(conn, userId, row);
        if (produkId == null) {
            JOptionPane.showMessageDialog(this, "Produk tidak ditemukan.");
            return;
        }

        // Ambil stok dari database
        String sqlStok = "SELECT stok FROM produk WHERE id_produk = ?";
        PreparedStatement pst = conn.prepareStatement(sqlStok);
        pst.setString(1, produkId);
        ResultSet rs = pst.executeQuery();
        int stok = 0;
        if (rs.next()) {
            stok = rs.getInt("stok");
        }

        String namaProduk = tabelkeranjang.getValueAt(row, 1).toString();
        int jumlahSekarang = Integer.parseInt(tabelkeranjang.getValueAt(row, 5).toString());

        if (jumlahSekarang >= stok) {
            JOptionPane.showMessageDialog(this, "Jumlah barang sudah mencapai batas stok.");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this,
                "Tambah 1 barang untuk \"" + namaProduk + "\"?",
                "Konfirmasi", JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            UserKeranjang.tambahJumlahBarang(conn, userId, produkId, 1);
            loadKeranjang(); // refresh tampilan
        }
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Kesalahan: " + ex.getMessage());
        ex.printStackTrace();
    }
    }//GEN-LAST:event_btntambahActionPerformed

    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        String userId = UserLogin.getSessionUser().getId();
        int row = tabelkeranjang.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Pilih dulu produk yang mau diupdate.");
            return;
        }

        String namaProduk = tabelkeranjang.getValueAt(row, 1).toString();

        try (Connection conn = DBKoneksi.getConnection()) {
            String produkId = UserKeranjang.getProdukIdByRow(conn, userId, row);
            if (produkId == null) {
                JOptionPane.showMessageDialog(this, "Produk tidak ditemukan.");
                return;
            }

            // Ambil stok dari produk
            String sql = "SELECT stok FROM produk WHERE id_produk = ?";
            int stok = 0;
            try (PreparedStatement ps = conn.prepareStatement(sql)) {
                ps.setString(1, produkId);
                try (ResultSet rs = ps.executeQuery()) {
                    if (rs.next()) {
                        stok = rs.getInt("stok");
                    } else {
                        JOptionPane.showMessageDialog(this, "Stok produk tidak ditemukan.");
                        return;
                    }
                }
            }

            String input = JOptionPane.showInputDialog(this,
                    "Masukkan jumlah baru untuk \"" + namaProduk + "\" (stok: " + stok + "):");
            if (input == null) return;

            int jumlahBaru;
            try {
                jumlahBaru = Integer.parseInt(input);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Jumlah tidak valid.");
                return;
            }

            if (jumlahBaru <= 0) {
                JOptionPane.showMessageDialog(this, "Jumlah tidak boleh nol atau kurang.");
                return;
            }

            if (jumlahBaru > stok) {
                JOptionPane.showMessageDialog(this, "Jumlah melebihi stok. Stok tersedia: " + stok);
                return;
            }

            int confirm = JOptionPane.showConfirmDialog(this,
                    "Update jumlah produk \"" + namaProduk + "\" menjadi " + jumlahBaru + "?",
                    "Konfirmasi", JOptionPane.YES_NO_OPTION);

            if (confirm == JOptionPane.YES_OPTION) {
                UserKeranjang.updateJumlahBarang(conn, userId, produkId, jumlahBaru);
                loadKeranjang();
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnupdateActionPerformed

    private void btnhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapusActionPerformed
    String userId = UserLogin.getSessionUser().getId();

    int row = tabelkeranjang.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Silakan pilih barang yang ingin dihapus.");
        return;
    }

    String namaProduk = tabelkeranjang.getValueAt(row, 1).toString();
    int confirm = JOptionPane.showConfirmDialog(this,
            "Apakah Anda yakin ingin menghapus produk '" + namaProduk + "' dari keranjang?",
            "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);

    if (confirm == JOptionPane.YES_OPTION) {
        try (Connection conn = DBKoneksi.getConnection()) {
            String produkId = UserKeranjang.getProdukIdByRow(conn, userId, row);
            if (produkId == null) {
                JOptionPane.showMessageDialog(this, "Produk tidak ditemukan.");
                return;
            }

            // Gunakan logika hapusItem yang telah diperbaiki sebelumnya
            UserKeranjang.hapusItem(conn, userId, produkId);
            JOptionPane.showMessageDialog(this, "Produk berhasil dihapus dari keranjang.");
            loadKeranjang();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            e.printStackTrace();
        }
    }
    }//GEN-LAST:event_btnhapusActionPerformed

