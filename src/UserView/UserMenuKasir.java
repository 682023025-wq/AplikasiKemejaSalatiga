    private void btnkeranjangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnkeranjangActionPerformed
    try (Connection conn = DBKoneksi.getConnection()) {
        String userId = UserLogin.getSessionUser().getId();
        ModelPembayaran pembayaran = UserPembayaran.getPembayaranPending(conn, userId);

        if (pembayaran == null) {
            JOptionPane.showMessageDialog(this, "Tidak ada transaksi", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        new UserMenuKeranjang().setVisible(true);
        dispose();

    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat memeriksa pembayaran.", "Error", JOptionPane.ERROR_MESSAGE);
    }

    }//GEN-LAST:event_btnkeranjangActionPerformed

