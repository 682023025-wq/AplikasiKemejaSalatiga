    private void tabelprodukMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelprodukMouseClicked
    try (Connection conn = DBKoneksi.getConnection()) {
        int row = tabelproduk.getSelectedRow();
        if (row == -1) return;

        String produkId = tabelproduk.getValueAt(row, 1).toString();
        long harga = Long.parseLong(tabelproduk.getValueAt(row, 5).toString());
        String userId = UserLogin.getSessionUser().getId();

        // Ambil stok produk dari DB
        int stok = 0;
        try (PreparedStatement ps = conn.prepareStatement("SELECT stok FROM produk WHERE id_produk = ?")) {
            ps.setString(1, produkId);
            ResultSet rs = ps.executeQuery();
            if (rs.next()) {
                stok = rs.getInt("stok");
            } else {
                JOptionPane.showMessageDialog(this, "Produk tidak ditemukan.");
                return;
            }
        }

        // Ambil jumlah total barang dari user yg BELUM lunas (status pending) untuk produk ini
        int totalSudahDipesan = 0;
        String sqlTotal = """
            SELECT COALESCE(SUM(dt.jumlah_barang), 0) as total 
            FROM detail_transaksi dt
            JOIN transaksi t ON dt.transaksi_id_transaksi = t.id_transaksi
            LEFT JOIN pembayaran p ON dt.pembayaran_order_id = p.order_id
            WHERE t.user_id = ? 
              AND dt.produk_id_produk = ?
              AND (p.status IS NULL OR p.status = 'pending')
        """;
        try (PreparedStatement ps = conn.prepareStatement(sqlTotal)) {
            ps.setString(1, userId);
            ps.setString(2, produkId);
            ResultSet rs = ps.executeQuery();
            if (rs.next()) {
                totalSudahDipesan = rs.getInt("total");
            }
        }

        // Input user
        String input = JOptionPane.showInputDialog(this, "Masukkan jumlah yang ingin ditambah:");
        if (input == null || input.isBlank()) return;

        int jumlahBaru = Integer.parseInt(input);
        if (jumlahBaru <= 0) {
            JOptionPane.showMessageDialog(this, "Jumlah tidak boleh nol atau negatif.");
            return;
        }

        int totalSetelahInput = totalSudahDipesan + jumlahBaru;
        if (totalSetelahInput > stok) {
            JOptionPane.showMessageDialog(this, """
                Jumlah melebihi stok!
                Sudah dipesan: %d
                Ingin tambah: %d
                Stok tersedia: %d
                Sisa yang bisa ditambah: %d
                """.formatted(totalSudahDipesan, jumlahBaru, stok, stok - totalSudahDipesan));
            return;
        }

        // Cari transaksi pending (jika ada)
        String idTransaksi = null;
        String sqlTransaksi = """
            SELECT t.id_transaksi
            FROM transaksi t
            JOIN detail_transaksi dt ON t.id_transaksi = dt.transaksi_id_transaksi
            JOIN pembayaran p ON dt.pembayaran_order_id = p.order_id
            WHERE p.status = 'pending' AND t.user_id = ?
            LIMIT 1
        """;
        try (PreparedStatement ps = conn.prepareStatement(sqlTransaksi)) {
            ps.setString(1, userId);
            ResultSet rs = ps.executeQuery();
            if (rs.next()) {
                idTransaksi = rs.getString("id_transaksi");
            }
        }

        if (idTransaksi == null) {
            idTransaksi = UserTransaksi.generateIdTransaksi(conn, userId);
        }

        // Cek apakah produk sudah ada dalam transaksi pending ini
        boolean sudahAda = false;
        int jumlahLama = 0;
        String sqlCek = """
            SELECT jumlah_barang FROM detail_transaksi 
            WHERE transaksi_id_transaksi = ? AND produk_id_produk = ? AND pembayaran_order_id IS NULL
        """;
        try (PreparedStatement ps = conn.prepareStatement(sqlCek)) {
            ps.setString(1, idTransaksi);
            ps.setString(2, produkId);
            ResultSet rs = ps.executeQuery();
            if (rs.next()) {
                jumlahLama = rs.getInt("jumlah_barang");
                sudahAda = true;
            }
        }

        if (sudahAda) {
            String sqlUpdate = """
                UPDATE detail_transaksi
                SET jumlah_barang = ?, total_harga = ?
                WHERE transaksi_id_transaksi = ? AND produk_id_produk = ? AND pembayaran_order_id IS NULL
            """;
            try (PreparedStatement ps = conn.prepareStatement(sqlUpdate)) {
                ps.setInt(1, jumlahLama + jumlahBaru);
                ps.setLong(2, harga * (jumlahLama + jumlahBaru));
                ps.setString(3, idTransaksi);
                ps.setString(4, produkId);
                ps.executeUpdate();
            }
        } else {
            ModelTransaksi transaksi = new ModelTransaksi();
            transaksi.setIdTransaksi(idTransaksi);
            transaksi.setTanggalTransaksi(new Date());
            transaksi.setUserId(userId);
            transaksi.setProdukId(produkId);
            transaksi.setHarga(harga);
            transaksi.setJumlahBarang(jumlahBaru);
            transaksi.setTotalHarga(harga * jumlahBaru);
            transaksi.setTotalPembayaran(harga * jumlahBaru);
            UserTransaksi.buatTransaksi(conn, transaksi);
        }

        JOptionPane.showMessageDialog(this, "Berhasil ditambahkan ke transaksi: " + idTransaksi);

    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_tabelprodukMouseClicked

