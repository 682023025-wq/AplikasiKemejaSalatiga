private void tampilkanLaporan() {
    SimpleDateFormat sdf = new SimpleDateFormat("EEEE, dd MMMM yyyy HH:mm", new Locale("id", "ID"));
    String periode = sdf.format(UserMenuSalesReport.tanggalDipilih);
 

     StringBuilder laporan = new StringBuilder();
    laporan.append("                        LAPORAN KEUANGAN\n\n");
     laporan.append("                       Periode: ").append(periode).append("\n\n");
    laporan.append(String.format("%-15s| %-20s| %-7s| %-9s| %-12s| %-12s\n",
            "ID Transaksi", "Nama Produk", "Jumlah", "Harga", "Total Harga", "Tanggal"));
    laporan.append("-----------------------------------------------------------------------------\n");

    int totalBarang = 0;
    double totalPenjualan = 0;

    UserSalesReport Controller = new UserSalesReport();
    List<ModelSalesReport> list = controller.getSalesReportsByDate(UserMenuSalesReport.tanggalDipilih);

    for (ModelSalesReport sr : list) {
        laporan.append(String.format("%-15s| %-20s| %-7d| %-9.2f| %-12.2f| %-12s\n",
                sr.getID_Transaksi(),
                sr.getNama(),
                sr.getJumlah_barang(),
                sr.getHarga_barang(),
                sr.getTotal_harga(),
                sdf.format(sr.getTanggal_transaksi())));

        totalBarang += sr.getJumlah_barang();
        totalPenjualan += sr.getTotal_harga();
    }

    laporan.append("\nTotal Barang Terjual : ").append(totalBarang);
    laporan.append(String.format("\nTotal Penjualan      : Rp %.2f", totalPenjualan));

    viewlaporan.setFont(new java.awt.Font("Courier New", java.awt.Font.PLAIN, 12));
    viewlaporan.setText(laporan.toString());
}

