    private void btncetakMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btncetakMouseClicked
    try {
        // Format tanggal jadi nama file
        SimpleDateFormat sdf = new SimpleDateFormat("EEEE-dd-MM-yyyy-HHmmss", new Locale("id", "ID"));
        String periode = sdf.format(UserMenuSalesReport.tanggalDipilih);
        String fileName = "laporan-" + periode + ".pdf";

        // Path ke folder tujuan
        String folderPath = "assets/laporan";
        java.io.File folder = new java.io.File(folderPath);
        if (!folder.exists()) {
            folder.mkdirs(); // buat folder jika belum ada
        }

        String filePath = folderPath + File.separator + fileName;

        // Setup PDF writer
        PdfWriter writer = new PdfWriter(filePath);
        PdfDocument pdf = new PdfDocument(writer);
        Document document = new Document(pdf);

        // Judul UserMenuLaporan
        document.add(new Paragraph("LAPORAN KEUANGAN")
            .setFontSize(14)
            .setTextAlignment(com.itextpdf.layout.properties.TextAlignment.CENTER));
        document.add(new Paragraph("Periode: " + periode)
            .setTextAlignment(com.itextpdf.layout.properties.TextAlignment.CENTER));
        document.add(new Paragraph("\n"));

        // Tabel isi
        com.itextpdf.layout.element.Table table = new com.itextpdf.layout.element.Table(6);
        table.addHeaderCell("ID Transaksi");
        table.addHeaderCell("Nama Produk");
        table.addHeaderCell("Jumlah");
        table.addHeaderCell("Harga");
        table.addHeaderCell("Total Harga");
        table.addHeaderCell("Tanggal");

        int totalBarang = 0;
        double totalPenjualan = 0;
        List<ModelSalesReport> list = controller.getAllSalesReports();

        for (ModelSalesReport sr : list) {
            table.addCell(sr.getID_Transaksi());
            table.addCell(sr.getNama());
            table.addCell(String.valueOf(sr.getJumlah_barang()));
            table.addCell(String.format("Rp %.2f", sr.getHarga_barang()));
            table.addCell(String.format("Rp %.2f", sr.getTotal_harga()));
            table.addCell(sdf.format(sr.getTanggal_transaksi()));

            totalBarang += sr.getJumlah_barang();
            totalPenjualan += sr.getTotal_harga();
        }

        NumberFormat rupiahFormat = NumberFormat.getInstance(new Locale("id", "ID"));

        document.add(table);
        document.add(new Paragraph("\n"));
        document.add(new Paragraph("Total Barang Terjual : " + totalBarang));
        document.add(new Paragraph("Total Penjualan      : Rp " + rupiahFormat.format(totalPenjualan)));

        document.close();
        JOptionPane.showMessageDialog(this, "PDF berhasil disimpan: " + filePath);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal cetak PDF: " + e.getMessage());
        e.printStackTrace();
    }
    }//GEN-LAST:event_btncetakMouseClicked

